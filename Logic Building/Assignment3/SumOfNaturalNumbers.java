//1. Write a program to calculate the sum of the first 50 natural numbers. 

public class SumOfNaturalNumbers {
    public static void main(String[] args) {
        int n = 50;
        int sum = (n * (n + 1)) / 2;
        System.out.println("Sum of the first 50 natural numbers: " + sum);
    }
}
